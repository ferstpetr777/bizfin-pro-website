        <div class="faq-answer">Если исполнитель спорит с требованием о возврате аванса, необходимо: собрать дополнительные доказательства нарушения, получить экспертные заключения, зафиксировать факт спора документально, обратиться в банк-гарант с полным пакетом документов, при необходимости обратиться в суд, использовать медиацию или арбитраж, защищать свои интересы через юристов.</div>
