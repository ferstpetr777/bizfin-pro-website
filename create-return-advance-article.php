<h2 id="terms">Сроки взыскания</h2>

<p>Сроки взыскания аванса по банковской гарантии имеют критическое значение для успешного возврата средств. Нарушение сроков может привести к потере права на взыскание и невозможности возврата аванса.</p>

<div class="structure-item">
    <h4>Сроки предъявления требования</h4>
    <p>Сроки предъявления требования определяются: условиями банковской гарантии, сроком действия гарантии, сроком исковой давности, условиями основного договора, требованиями законодательства, практикой банка-гаранта, особенностями конкретной ситуации.</p>
</div>

<div class="structure-item">
    <h4>Срок действия гарантии</h4>
    <p>Срок действия гарантии обычно составляет: от 1 месяца до 2 лет, в зависимости от типа договора, сложности проекта, требований заказчика, условий банка, особенностей отрасли, рисков проекта, опыта принципала.</p>
</div>

<div class="structure-item">
    <h4>Срок рассмотрения требования</h4>
    <p>Банк-гарант обязан рассмотреть требование в течение: 5 рабочих дней (стандартный срок), 3 рабочих дня (при наличии всех документов), 7 рабочих дней (при необходимости дополнительной проверки), 10 рабочих дней (в сложных случаях), в соответствии с условиями гарантии.</p>
</div>

<div class="structure-item">
    <h4>Срок выплаты</h4>
    <p>После принятия положительного решения банк обязан выплатить средства в течение: 1 рабочего дня (при наличии средств), 3 рабочих дней (стандартный срок), 5 рабочих дней (при больших суммах), 7 рабочих дней (в исключительных случаях), в соответствии с условиями гарантии.</p>
</div>

