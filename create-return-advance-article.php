        <div class="faq-answer">Возврат аванса частично возможен в случаях: частичного неисполнения обязательств, частичного нарушения сроков, частичного некачественного выполнения работ, частичного нецелевого использования аванса. Размер частичного возврата определяется пропорционально степени нарушения и условиями банковской гарантии.</div>
    </div>
    
