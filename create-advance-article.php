<h2 id="calculation">Расчёт размера гарантии</h2>

<p>Расчёт размера банковской гарантии авансового платежа зависит от множества факторов, включая сумму аванса, тип контракта, риски проекта и требования заказчика. Правильный расчёт гарантии критически важен для успешного получения аванса и минимизации финансовых рисков.</p>

<div class="structure-item">
    <h4>Основные принципы расчёта</h4>
    <p>Размер гарантии рассчитывается на основе: суммы авансового платежа (обычно 100% от аванса), дополнительных рисков проекта (5-15% от суммы аванса), требований заказчика (может превышать сумму аванса), типа контракта (государственные закупки имеют особые требования), срока выполнения работ, сложности проекта.</p>
</div>

<div class="structure-item">
    <h4>Формула расчёта</h4>
    <p>Базовая формула: Размер гарантии = Сумма аванса + Резерв на риски (5-15%) + Дополнительные требования заказчика. Для государственных закупок: Размер гарантии = Сумма аванса × Коэффициент риска (1.0-1.2). Для коммерческих контрактов: Размер гарантии = Сумма аванса + Фиксированная надбавка (100-500 тыс. руб.).</p>
</div>

<div class="structure-item">
    <h4>Факторы влияния на размер</h4>
    <p>На размер гарантии влияют: сумма авансового платежа, срок выполнения контракта, сложность работ, опыт подрядчика, кредитная история, финансовое состояние, требования заказчика, тип контракта, отраслевые риски, экономическая ситуация.</p>
</div>

<div class="structure-item">
    <h4>Примеры расчётов</h4>
    <p>Пример 1: Аванс 1 млн руб. → Гарантия 1.1 млн руб. (110%). Пример 2: Аванс 5 млн руб. → Гарантия 5.5 млн руб. (110%). Пример 3: Аванс 10 млн руб. → Гарантия 11 млн руб. (110%). Пример 4: Государственный контракт, аванс 20 млн руб. → Гарантия 22 млн руб. (110%).</p>
</div>

<div class="info">
    <strong>Важно:</strong> Размер банковской гарантии не может быть меньше суммы авансового платежа. Заказчики часто требуют гарантию на 110-120% от суммы аванса для покрытия дополнительных рисков и возможных штрафов.
</div>

<h2 id="terms">Срок действия гарантии</h2>

<p>Срок действия банковской гарантии авансового платежа должен покрывать весь период выполнения контракта плюс дополнительное время для устранения возможных недостатков. Правильное определение срока гарантии критически важно для защиты интересов заказчика.</p>

<div class="step-guide">
    <h4>Расчёт срока действия</h4>
    <ol>
        <li>Срок выполнения основного контракта</li>
        <li>Период гарантийных обязательств (обычно 12-24 месяца)</li>
        <li>Время на устранение недостатков (30-90 дней)</li>
        <li>Резерв на возможные задержки (10-20% от срока)</li>
        <li>Время на взыскание по гарантии (30-60 дней)</li>
        <li>Итоговый срок = Сумма всех периодов</li>
    </ol>
</div>

<div class="structure-item">
    <h4>Минимальные требования</h4>
    <p>Минимальный срок гарантии должен: превышать срок выполнения контракта минимум на 1 месяц, включать период гарантийных обязательств, предусматривать время на устранение недостатков, учитывать возможные задержки, соответствовать требованиям заказчика, быть достаточным для всех этапов работ.</p>
</div>

<div class="structure-item">
    <h4>Типичные сроки по отраслям</h4>
    <p>IT-разработка: 6-18 месяцев. Строительство: 12-36 месяцев. Поставка оборудования: 3-12 месяцев. Консалтинг: 3-12 месяцев. Производство: 6-24 месяца. Услуги: 3-12 месяцев. Государственные закупки: согласно требованиям 44-ФЗ и 223-ФЗ.</p>
</div>

<div class="structure-item">
