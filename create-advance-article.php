<h2 id="documents">Документы для оформления</h2>

<p>Для оформления банковской гарантии авансового платежа необходимо подготовить полный пакет документов, соответствующий требованиям банка и специфике конкретного контракта. Неполный или некорректный пакет документов может привести к отказу в выдаче гарантии.</p>

<div class="structure-item">
    <h4>Обязательные документы</h4>
    <p>Базовый пакет включает: заявление на выдачу банковской гарантии, учредительные документы компании, финансовую отчетность за последние 2 года, налоговые декларации и справки, банковские выписки за последние 6 месяцев, договор с заказчиком, техническое задание или спецификацию, справки о состоянии расчетов.</p>
</div>

<div class="structure-item">
    <h4>Документы по контракту</h4>
    <p>По контракту необходимы: основной договор с заказчиком, техническое задание, спецификация работ или товаров, календарный план выполнения, смета или расчет стоимости, документы о квалификации исполнителя, лицензии и сертификаты, опыт выполнения аналогичных работ, рекомендательные письма.</p>
</div>

<div class="structure-item">
    <h4>Финансовые документы</h4>
