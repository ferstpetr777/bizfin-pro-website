<h2 id="verification">Проверка банком</h2>

<p>Банк проводит комплексную проверку заявителя и проекта перед выдачей банковской гарантии авансового платежа. Понимание процесса проверки поможет правильно подготовиться к получению гарантии и избежать отказов.</p>

<div class="structure-item">
    <h4>Этапы проверки</h4>
    <p>Процесс проверки включает: первичную проверку документов (1-2 дня), анализ финансового состояния (2-3 дня), оценку кредитной истории (1 день), анализ проекта и рисков (2-3 дня), принятие решения (1 день), оформление гарантии (1 день). Общий срок: 5-10 рабочих дней.</p>
</div>

<div class="structure-item">
    <h4>Финансовый анализ</h4>
    <p>Банк анализирует: финансовое состояние компании, ликвидность и платежеспособность, рентабельность деятельности, структуру активов и пассивов, денежные потоки, кредитную историю, наличие просроченной задолженности, соответствие финансовых показателей нормативным требованиям.</p>
</div>

<div class="structure-item">
    <h4>Анализ проекта</h4>
    <p>Оценка проекта включает: реалистичность сроков выполнения, соответствие возможностей исполнителя объему работ, наличие необходимых ресурсов, опыт выполнения аналогичных проектов, квалификацию персонала, материально-техническую базу, риски проекта, экономическую эффективность.</p>
</div>

<div class="structure-item">
    <h4>Критерии одобрения</h4>
    <p>Банк одобряет гарантию при: стабильном финансовом положении, положительной кредитной истории, наличии опыта выполнения аналогичных работ, реалистичности проекта, достаточности ресурсов, отсутствии просроченной задолженности, соответствии требованиям банка, готовности к дополнительному обеспечению.</p>
</div>

<div class="error">
