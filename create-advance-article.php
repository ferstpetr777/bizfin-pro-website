<h2 id="faq">Часто задаваемые вопросы</h2>

<div class="faq-section">
    <div class="faq-item">
        <div class="faq-question">Как учитывается банковская гарантия при предоплате партиями?</div>
        <div class="faq-answer">При предоплате партиями банковская гарантия оформляется на общую сумму всех авансовых платежей или на каждую партию отдельно. Рекомендуется оформлять единую гарантию на всю сумму аванса с условием поэтапного снижения по мере выполнения работ. Это упрощает документооборот и снижает общую стоимость гарантии.</div>
    </div>
    
    <div class="faq-item">
        <div class="faq-question">Можно ли изменить размер гарантии в процессе выполнения контракта?</div>
        <div class="faq-answer">Да, размер гарантии можно изменить при согласии всех сторон (банка, принципала, бенефициара). Обычно это делается при изменении суммы контракта или графика платежей. Изменения оформляются дополнительным соглашением к договору банковской гарантии с соответствующей корректировкой комиссии.</div>
    </div>
    
    <div class="faq-item">
        <div class="faq-question">Что делать, если заказчик требует гарантию больше суммы аванса?</div>
        <div class="faq-answer">Если заказчик требует гарантию больше суммы аванса, необходимо: обосновать необходимость увеличения, согласовать дополнительные условия с банком, учесть увеличение комиссии, оформить дополнительное соглашение, убедиться в соответствии требованиям законодательства, минимизировать риски для принципала.</div>
    </div>
    
    <div class="faq-item">
        <div class="faq-question">Как влияет срок действия контракта на стоимость гарантии?</div>
        <div class="faq-answer">Срок действия контракта напрямую влияет на стоимость гарантии: чем дольше срок, тем выше комиссия банка. Обычно применяется прогрессивная шкала: до 6 месяцев - базовая ставка, 6-12 месяцев - +0.5%, 12-24 месяца - +1%, свыше 24 месяцев - +1.5% и более.</div>
    </div>
    
    <div class="faq-item">
        <div class="faq-question">Можно ли использовать одну гарантию для нескольких контрактов?</div>
        <div class="faq-answer">Обычно одна банковская гарантия выдается для конкретного контракта. Однако некоторые банки предлагают рамочные гарантии для работы с одним заказчиком по нескольким контрактам. Необходимо уточнить условия в банке и требования конкретного заказчика.</div>
    </div>
    
    <div class="faq-item">
        <div class="faq-question">Что происходит с гарантией при досрочном выполнении контракта?</div>
        <div class="faq-answer">При досрочном выполнении контракта банковская гарантия может быть: досрочно прекращена по соглашению сторон, продолжать действовать до истечения срока, частично снижена при частичном выполнении. Рекомендуется согласовать условия досрочного прекращения гарантии в договоре.</div>
    </div>
</div>

