<h2 id="verification">Проверка</h2>

<p>Проверка банком заявителя и проекта перед выдачей гарантии возврата аванса является обязательным этапом процесса. Понимание критериев и процедуры проверки поможет правильно подготовиться к получению гарантии и избежать отказов.</p>

<div class="structure-item">
    <h4>Этапы проверки</h4>
    <p>Процесс проверки включает: первичную проверку документов (1-2 дня), анализ финансового состояния (2-3 дня), оценку кредитной истории (1 день), анализ проекта и рисков (2-3 дня), проверку контрагентов (1-2 дня), принятие решения (1 день), оформление гарантии (1 день). Общий срок: 7-12 рабочих дней.</p>
</div>

<div class="structure-item">
    <h4>Финансовый анализ</h4>
    <p>Банк анализирует: финансовое состояние компании, ликвидность и платежеспособность, рентабельность деятельности, структуру активов и пассивов, денежные потоки, кредитную историю, наличие просроченной задолженности, соответствие финансовых показателей нормативным требованиям, наличие залогового обеспечения.</p>
</div>

<div class="structure-item">
    <h4>Анализ проекта</h4>
    <p>Оценка проекта включает: реалистичность сроков выполнения, соответствие возможностей исполнителя объему работ, наличие необходимых ресурсов, опыт выполнения аналогичных проектов, квалификацию персонала, материально-техническую базу, риски проекта, экономическую эффективность, соответствие законодательству.</p>
</div>

<div class="structure-item">
    <h4>Проверка контрагентов</h4>
    <p>Анализ контрагентов включает: финансовое состояние заказчика, репутацию и деловую активность, опыт работы с аналогичными проектами, наличие судебных споров, кредитную историю, соответствие требованиям банка, готовность к сотрудничеству, прозрачность бизнес-процессов.</p>
</div>

<div class="warning">
