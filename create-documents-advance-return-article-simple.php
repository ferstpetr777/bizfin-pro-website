<h3>Документы по государственному контракту</h3>
<p>При участии в государственных закупках предоставляются извещение о проведении закупки, документация о закупке, проект контракта, протоколы рассмотрения заявок и подведения итогов, а также подписанный контракт, если он уже заключен.</p>

