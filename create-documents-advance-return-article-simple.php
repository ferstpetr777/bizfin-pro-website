<h2 id="financial-documents">Финансовые документы и отчетность</h2>

<p>Финансовые документы являются ключевым элементом при принятии решения о выдаче банковской гарантии. Банк анализирует финансовое состояние компании, оценивает ее платежеспособность и способность выполнить обязательства по контракту.</p>

<h3>Бухгалтерская отчетность</h3>
<p>Предоставляется полная бухгалтерская отчетность за последние 2 года, включающая бухгалтерский баланс, отчет о финансовых результатах, отчет о движении денежных средств и пояснения к бухгалтерской отчетности. Все документы должны быть заверены налоговой инспекцией и содержать расшифровки основных статей.</p>

<h3>Налоговые документы</h3>
<p>Обязательно предоставление налоговых деклараций по налогу на прибыль и НДС за последние 4 квартала, справок об отсутствии задолженности по налогам и сборам, страховым взносам, а также справки о состоянии расчетов с бюджетом.</p>

