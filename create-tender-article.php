<h2 id="tender-requirements">Требования тендеров</h2>

<p>Требования к банковским гарантиям в тендерах строго регламентированы законодательством о закупках и могут существенно различаться в зависимости от типа закупки, суммы контракта и специфики заказчика. Понимание этих требований критически важно для успешного участия в тендерах.</p>

<div class="structure-item">
    <h4>Обязательные требования по 44-ФЗ</h4>
    <p>Согласно 44-ФЗ банковская гарантия должна: быть безотзывной, содержать сумму гарантии, срок действия, условия выплаты, реквизиты банка-гаранта, реквизиты принципала и бенефициара, перечень документов для выплаты, условия прекращения обязательств, соответствовать требованиям Минфина РФ.</p>
</div>

<div class="structure-item">
    <h4>Требования по 223-ФЗ</h4>
    <p>Для закупок по 223-ФЗ требования могут быть: более гибкими по сравнению с 44-ФЗ, устанавливаться заказчиком самостоятельно, включать дополнительные условия, предусматривать различные виды обеспечения, иметь специфические требования к банкам, включать особые условия выплаты.</p>
</div>

<div class="structure-item">
    <h4>Размер гарантии</h4>
    <p>Размер банковской гарантии зависит от: суммы контракта (обычно 5-30%), типа закупки, требований заказчика, специфики работ, рисков проекта. Для государственных закупок по 44-ФЗ размер составляет 5-10% от НМЦК, для коммерческих закупок может достигать 30%.</p>
</div>

<div class="structure-item">
    <h4>Срок действия гарантии</h4>
    <p>Срок действия должен: превышать срок исполнения контракта минимум на 1 месяц, учитывать возможные задержки, предусматривать время на устранение недостатков, включать период гарантийных обязательств, соответствовать требованиям заказчика, быть достаточным для всех этапов работ.</p>
</div>

<div class="info">
    <strong>Важно:</strong> Банковская гарантия должна быть включена в реестр банковских гарантий на сайте zakupki.gov.ru. Гарантии, не включенные в реестр, не принимаются заказчиками в государственных закупках.
</div>

