        <div class="faq-answer">Гарантия возврата предоплаты обеспечивает возврат предоплаченных средств, если подрядчик не выполнит свои обязательства. Гарантия исполнения контракта обеспечивает выполнение всех условий контракта в целом, включая сроки, качество и объем работ. Они могут использоваться как по отдельности, так и совместно для максимальной защиты интересов заказчика.</div>
    </div>
    
