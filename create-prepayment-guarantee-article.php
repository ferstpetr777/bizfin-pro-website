<h2 id="amount">Сумма банковской гарантии возврата предоплаты</h2>

<p>Размер банковской гарантии возврата предоплаты обычно составляет 100-110% от суммы предоплаты. Это позволяет заказчику не только вернуть предоплаченные средства, но и покрыть возможные дополнительные расходы, связанные с поиском нового исполнителя или завершением проекта.</p>

<div class="structure-item">
    <h4>Факторы, влияющие на размер гарантии:</h4>
    <ul>
        <li><strong>Размер предоплаты:</strong> Чем больше предоплата, тем выше сумма гарантии</li>
        <li><strong>Отрасль проекта:</strong> В высокорисковых отраслях процент покрытия может быть выше</li>
        <li><strong>Репутация подрядчика:</strong> Для надежных компаний возможен минимальный процент покрытия</li>
        <li><strong>Требования заказчика:</strong> Заказчик может установить свои требования к размеру гарантии</li>
        <li><strong>Сроки выполнения:</strong> Длительные проекты требуют более высокого покрытия</li>
        <li><strong>Сложность проекта:</strong> Сложные проекты увеличивают риски и размер гарантии</li>
    </ul>
</div>

<div class="info">
    <p><strong>Формула расчёта:</strong> Сумма гарантии = Сумма предоплаты × (1 + Процент покрытия рисков)</p>
</div>

<div class="structure-item">
    <h4>Примеры расчёта суммы гарантии:</h4>
    <ul>
        <li><strong>Предоплата 2 млн руб.:</strong> Гарантия 2-2.2 млн руб.</li>
        <li><strong>Предоплата 5 млн руб.:</strong> Гарантия 5-5.5 млн руб.</li>
        <li><strong>Предоплата 10 млн руб.:</strong> Гарантия 10-11 млн руб.</li>
        <li><strong>Предоплата 20 млн руб.:</strong> Гарантия 20-22 млн руб.</li>
    </ul>
</div>

<p>Точный размер гарантии всегда согласовывается с банком и заказчиком, учитывая все особенности конкретного проекта и договора.</p>

