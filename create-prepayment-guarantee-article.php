<h2 id="faq">Часто задаваемые вопросы</h2>

<div class="faq-section">
    <div class="faq-item">
        <div class="faq-question">При частичной предоплате?</div>
        <div class="faq-answer">При частичной предоплате банковская гарантия может быть оформлена на общую сумму предоплаты или на каждую часть отдельно. В случае поэтапной предоплаты, гарантия может быть уменьшающейся, то есть её сумма будет снижаться по мере освоения предоплаченных средств. Это позволяет оптимизировать расходы на комиссию и обеспечить гибкость в управлении рисками.</div>
    </div>
    
    <div class="faq-item">
