<p>Срок действия банковской гарантии возврата предоплаты должен покрывать весь период освоения предоплаты и, как правило, превышать его на 1-2 месяца. Это дает заказчику дополнительное время для предъявления требования в случае возникновения проблем.</p>

<div class="step-guide">
    <h4>Как определить срок действия гарантии:</h4>
    <ol>
        <li><strong>Определите срок освоения предоплаты:</strong> Согласно договору, это может быть несколько месяцев или год</li>
        <li><strong>Добавьте буферный период:</strong> Обычно 30-60 дней после предполагаемого срока освоения</li>
        <li><strong>Учтите возможные задержки:</strong> В сложных проектах могут быть непредвиденные обстоятельства</li>
        <li><strong>Согласуйте с банком:</strong> Банк может предложить стандартные сроки или индивидуальные условия</li>
        <li><strong>Проверьте требования заказчика:</strong> Некоторые заказчики устанавливают минимальный срок действия гарантии</li>
        <li><strong>Убедитесь в достаточности срока:</strong> Гарантия должна действовать до полного выполнения обязательств по предоплате</li>
    </ol>
</div>

<div class="structure-item">
    <h4>Типичные сроки действия гарантии:</h4>
    <ul>
        <li><strong>Краткосрочные проекты (до 3 месяцев):</strong> 4-5 месяцев</li>
        <li><strong>Среднесрочные проекты (3-6 месяцев):</strong> 7-8 месяцев</li>
        <li><strong>Долгосрочные проекты (6-12 месяцев):</strong> 13-14 месяцев</li>
