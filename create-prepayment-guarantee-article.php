<h2 id="verification">Проверка банком перед выдачей гарантии</h2>

<p>Процесс проверки банком включает анализ финансового состояния подрядчика, оценку рисков проекта и проверку юридической чистоты сделки. Это позволяет банку убедиться в способности подрядчика выполнить свои обязательства и минимизировать собственные риски.</p>

<div class="structure-item">
    <h4>Этапы проверки банком:</h4>
    <ul>
        <li><strong>Финансовый анализ:</strong> Оценка платежеспособности и кредитоспособности компании</li>
        <li><strong>Юридическая экспертиза:</strong> Проверка учредительных документов, полномочий лиц, подписывающих договор</li>
        <li><strong>Анализ контракта:</strong> Изучение условий основного договора, графика освоения предоплаты</li>
        <li><strong>Оценка обеспечения:</strong> Если предоставлено, оценка ликвидности залога или надежности поручителя</li>
        <li><strong>Проверка репутации:</strong> Анализ кредитной истории, судебных споров, отзывов</li>
