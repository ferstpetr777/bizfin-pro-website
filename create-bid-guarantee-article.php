    <h4>1. Неправильное указание суммы</h4>
    <p>Сумма гарантии должна точно соответствовать требованиям заказчика. Ошибка даже в 1 рубле может привести к отклонению заявки.</p>
    
    <h4>2. Некорректные сроки</h4>
    <p>Срок действия гарантии должен покрывать весь период от подачи заявки до подписания контракта плюс запас.</p>
    
