<div class="case-study">
    <h4>Кейс 1: Строительство торгового центра</h4>
    <p><strong>Ситуация:</strong> ООО "СтройИнвест" получило контракт на строительство торгового центра стоимостью 200 млн рублей. Срок выполнения — 18 месяцев. Размер гарантии — 15% (30 млн рублей).</p>
    <p><strong>Проблема:</strong> На 12-м месяце выполнения работ у подрядчика возникли финансовые трудности, что привело к задержке сроков на 3 месяца.</p>
    <p><strong>Решение:</strong> Заказчик предъявил требование по банковской гарантии. Банк выплатил 20 млн рублей для покрытия ущерба от задержки. Оставшиеся 10 млн рублей были использованы для завершения работ другим подрядчиком.</p>
    <p><strong>Результат:</strong> Проект был завершен в срок, заказчик получил полную компенсацию ущерба.</p>
</div>

<div class="case-study">
    <h4>Кейс 2: Поставка медицинского оборудования</h4>
    <p><strong>Ситуация:</strong> ООО "МедТех" заключило контракт на поставку медицинского оборудования для больницы на сумму 50 млн рублей. Гарантия составила 10% (5 млн рублей).</p>
    <p><strong>Проблема:</strong> Поставленное оборудование не соответствовало техническим требованиям контракта. Заказчик потребовал замены оборудования.</p>
    <p><strong>Решение:</strong> Банк по гарантии выплатил 3 млн рублей на демонтаж некачественного оборудования и 2 млн рублей на покрытие ущерба от простоя больницы.</p>
    <p><strong>Результат:</strong> Оборудование было заменено, больница возобновила работу в полном объеме.</p>
</div>

