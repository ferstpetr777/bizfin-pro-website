<h2 id="faq">Часто задаваемые вопросы</h2>

<div class="faq">
    <h4>Что происходит с банковской гарантией при изменении цены контракта?</h4>
    <p>При изменении цены контракта необходимо пересчитать размер банковской гарантии пропорционально новому размеру контракта. Если цена увеличилась, потребуется доплата за дополнительное обеспечение. Если цена уменьшилась, банк может вернуть часть комиссии пропорционально снижению суммы гарантии.</p>
</div>

<div class="faq">
    <h4>Можно ли изменить условия банковской гарантии после её выдачи?</h4>
    <p>Банковская гарантия является безотзывным обязательством, но при изменении условий контракта можно выпустить дополнительное соглашение к гарантии или получить новую гарантию с обновленными условиями. Это потребует дополнительных расходов и времени.</p>
</div>

<div class="faq">
    <h4>Как рассчитывается размер гарантии для сложных многоэтапных проектов?</h4>
    <p>Для многоэтапных проектов размер гарантии рассчитывается с учетом рисков каждого этапа. Обычно используется прогрессивное снижение размера гарантии по мере выполнения этапов проекта. Например, 100% на начальном этапе, 70% на основном, 30% на завершающем.</p>
</div>

<div class="faq">
    <h4>Что делать, если банк отказывается выплачивать по гарантии?</h4>
    <p>При отказе банка в выплате по гарантии необходимо проверить соответствие требования заказчика условиям гарантии. Если требование соответствует условиям, можно обратиться в суд для принудительного взыскания. Также можно обратиться в Центральный банк РФ с жалобой на действия банка.</p>
</div>

<div class="faq">
    <h4>Можно ли использовать одну гарантию для нескольких контрактов?</h4>
    <p>Нет, каждая банковская гарантия привязана к конкретному контракту. Для каждого контракта необходимо получать отдельную гарантию с указанием всех реквизитов конкретного договора. Исключение составляют рамочные соглашения, где может быть предусмотрена единая гарантия.</p>
</div>

<div class="faq">
    <h4>Как влияет на размер гарантии опыт выполнения аналогичных проектов?</h4>
    <p>Опыт выполнения аналогичных проектов положительно влияет на размер гарантии. Банки могут снизить размер гарантии на 1-3% для компаний с успешным опытом выполнения аналогичных проектов. Это связано с более низкими рисками для банка.</p>
</div>

<div class="faq">
    <h4>Что происходит с гарантией при досрочном расторжении контракта?</h4>
    <p>При досрочном расторжении контракта банковская гарантия прекращает свое действие. Банк возвращает залогодателю часть комиссии пропорционально неиспользованному сроку действия гарантии, если это предусмотрено договором. Также необходимо уведомить банк о расторжении контракта.</p>
</div>

<div class="faq">
