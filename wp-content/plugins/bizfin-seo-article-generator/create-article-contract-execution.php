<h2 id="terms">Срок действия банковской гарантии на исполнение контракта</h2>

<p>Срок действия банковской гарантии на исполнение контракта — критически важный параметр, который определяет период защиты интересов заказчика.</p>

<h3>Общие принципы определения срока</h3>

<p>Срок действия гарантии должен покрывать:</p>

<ul>
    <li><strong>Период выполнения контракта</strong> — от начала до полного завершения работ</li>
    <li><strong>Гарантийный период</strong> — время после сдачи работ для выявления недостатков</li>
    <li><strong>Резерв времени</strong> — дополнительный период на случай задержек</li>
</ul>

<h3>Минимальные требования к сроку</h3>

<p>Согласно законодательству, срок действия гарантии должен:</p>

<ul>
    <li>Превышать срок действия контракта не менее чем на 1 месяц</li>
    <li>Покрывать весь период выполнения обязательств</li>
    <li>Включать гарантийный период (если предусмотрен контрактом)</li>
</ul>

<h3>Расчёт оптимального срока</h3>

<p>Для расчёта оптимального срока гарантии используйте формулу:</p>

<div class="example">
    <p><strong>Срок гарантии = Срок контракта + Гарантийный период + Резерв (30 дней)</strong></p>
    <p>Пример: Контракт на 12 месяцев + гарантийный период 6 месяцев + резерв 1 месяц = гарантия на 19 месяцев</p>
</div>

<h3>Продление срока гарантии</h3>

<p>В случае необходимости срок гарантии может быть продлён:</p>

<ul>
    <li><strong>По соглашению сторон</strong> — при изменении сроков контракта</li>
    <li><strong>При форс-мажорных обстоятельствах</strong> — при объективных задержках</li>
    <li><strong>При изменении объёма работ</strong> — при увеличении стоимости контракта</li>
</ul>

