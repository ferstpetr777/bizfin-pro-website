<h2 id="purpose">Назначение банковской гарантии на исполнение контракта</h2>

<p>Банковская гарантия на исполнение контракта служит надежным механизмом защиты интересов заказчика и обеспечения выполнения обязательств поставщиком. Это финансовый инструмент, который минимизирует риски неисполнения договорных обязательств.</p>

<h3>Основные функции гарантии</h3>

<p>Банковская гарантия на исполнение контракта выполняет несколько ключевых функций:</p>

<ul>
    <li><strong>Финансовая защита заказчика</strong> — гарантирует возмещение ущерба в случае неисполнения контракта</li>
    <li><strong>Стимулирование добросовестного исполнения</strong> — мотивирует поставщика выполнить обязательства качественно и в срок</li>
    <li><strong>Снижение рисков для заказчика</strong> — минимизирует финансовые потери при нарушении договора</li>
    <li><strong>Повышение доверия между сторонами</strong> — создает основу для долгосрочного сотрудничества</li>
</ul>

<h3>Когда требуется гарантия исполнения</h3>

<p>Гарантия на исполнение контракта требуется в следующих случаях:</p>

<ul>
    <li>Государственные и муниципальные закупки (44-ФЗ, 223-ФЗ)</li>
    <li>Крупные коммерческие контракты</li>
    <li>Строительные и подрядные работы</li>
    <li>Поставка дорогостоящего оборудования</li>
    <li>Контракты с новыми или малоизвестными поставщиками</li>
</ul>

<h3>Отличие от других видов гарантий</h3>

<p>Гарантия на исполнение контракта отличается от других видов банковских гарантий:</p>

<ul>
    <li><strong>От гарантии возврата аванса</strong> — покрывает исполнение всего контракта, а не только возврат аванса</li>
    <li><strong>От тендерной гарантии</strong> — действует на протяжении всего срока выполнения контракта</li>
    <li><strong>От гарантии качества</strong> — покрывает не только качество, но и сроки, объемы выполнения</li>
</ul>

<h2 id="calculation">Расчёт суммы банковской гарантии на исполнение контракта</h2>

