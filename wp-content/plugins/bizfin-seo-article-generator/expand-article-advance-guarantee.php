        <p>Минимальная сумма аванса для банковской гарантии варьируется от 50 000 до 200 000 рублей в зависимости от банка. Для крупных банков минимальная сумма обычно составляет 100 000 рублей. При меньших суммах банки могут предложить альтернативные решения или отказать в выдаче гарантии из-за нерентабельности операции.</p>
    </div>

    <div class="faq-item">
        <h3>Можно ли получить гарантию без залога?</h3>
        <p>Да, многие банки выдают банковские гарантии на возврат аванса без залога при условии хорошего финансового состояния компании. Однако наличие залога или поручительства может снизить стоимость гарантии и ускорить процесс одобрения. Для крупных сумм (свыше 50 млн рублей) банк может потребовать обеспечение.</p>
    </div>

    <div class="faq-item">
