<h2 id="consolidated-reporting">Консолидированная отчётность для групп компаний</h2>

<p>Для групп компаний банки требуют консолидированную отчетность, которая отражает финансовое состояние всей группы в целом. Это позволяет банку оценить реальные финансовые возможности и риски.</p>

<h3>Что включает консолидированная отчетность</h3>

<p>Консолидированная отчетность должна включать:</p>

<ul>
    <li><strong>Консолидированный баланс</strong> — сводный баланс всех компаний группы</li>
    <li><strong>Консолидированный отчет о прибылях и убытках</strong> — сводный финансовый результат</li>
    <li><strong>Консолидированный отчет о движении денежных средств</strong> — денежные потоки группы</li>
    <li><strong>Пояснительная записка</strong> — описание структуры группы и принципов консолидации</li>
    <li><strong>Аудиторское заключение</strong> — подтверждение достоверности отчетности</li>
</ul>

<h3>Принципы консолидации</h3>

<p>При составлении консолидированной отчетности применяются следующие принципы:</p>

<ul>
    <li><strong>Исключение внутригрупповых операций</strong> — взаимные расчеты между компаниями группы</li>
    <li><strong>Единая учетная политика</strong> — применение одинаковых принципов учета</li>
    <li><strong>Валютная консолидация</strong> — пересчет в единую валюту отчетности</li>
    <li><strong>Временная консолидация</strong> — составление на одну отчетную дату</li>
</ul>

<h3>Преимущества консолидированной отчетности</h3>

<p>Предоставление консолидированной отчетности дает следующие преимущества:</p>

<ul>
    <li><strong>Повышение лимитов</strong> — банк может установить больший лимит гарантий</li>
    <li><strong>Снижение ставок</strong> — более выгодные условия по комиссии</li>
    <li><strong>Упрощение процедур</strong> — единый пакет документов для всех компаний группы</li>
    <li><strong>Централизованное управление</strong> — единая точка принятия решений</li>
</ul>

<h3>Требования к аудиту</h3>

<p>Для крупных групп компаний банки требуют аудированную отчетность:</p>

<ul>
    <li><strong>Обязательный аудит</strong> — для компаний с выручкой свыше 400 млн рублей</li>
    <li><strong>Аудиторская фирма</strong> — должна быть включена в реестр аудиторов</li>
    <li><strong>Мнение аудитора</strong> — безоговорочно положительное или с оговорками</li>
    <li><strong>Срок аудита</strong> — не более 6 месяцев с даты составления отчетности</li>
</ul>

