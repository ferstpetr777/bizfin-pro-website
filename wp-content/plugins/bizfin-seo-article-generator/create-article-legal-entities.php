<h2 id="requirements">Требования банков к юридическим лицам</h2>

<p>Банки предъявляют строгие требования к юридическим лицам при выдаче банковских гарантий. Понимание этих требований критически важно для успешного получения гарантии и участия в крупных проектах.</p>

<h3>Финансовые требования</h3>

<p>Банки анализируют финансовое состояние компании по следующим критериям:</p>

<div class="requirements-list">
    <h4>Ключевые финансовые показатели для получения банковской гарантии:</h4>
    <ul>
        <li><strong>Выручка за последние 2 года</strong> — стабильная или растущая динамика</li>
        <li><strong>Рентабельность</strong> — чистая прибыль не менее 3-5% от выручки</li>
        <li><strong>Ликвидность</strong> — коэффициент текущей ликвидности не менее 1,2</li>
        <li><strong>Финансовая устойчивость</strong> — коэффициент автономии не менее 0,3</li>
        <li><strong>Оборачиваемость дебиторской задолженности</strong> — не более 90 дней</li>
