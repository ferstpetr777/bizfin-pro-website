<h2 id="kalkulyator-alternativ">Калькулятор альтернатив</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Калькулятор альтернатив поможет вам быстро оценить экономическую эффективность выбора между банковской гарантией и залогом для конкретной ситуации.</p>
<!-- /wp:paragraph -->

<!-- wp:html -->
<div class="calculator-box">
    <h4>Калькулятор выбора инструмента обеспечения</h4>
    
    <p><strong>Входные параметры:</strong></p>
    <ul>
        <li>Сумма обеспечения (руб.)</li>
        <li>Срок действия (месяцев)</li>
        <li>Премия по гарантии (%)</li>
        <li>Альтернативная доходность залогового имущества (%)</li>
        <li>Стоимость обслуживания залога (руб./месяц)</li>
    </ul>
    
    <p><strong>Расчётные показатели:</strong></p>
    <ul>
        <li>Общая стоимость гарантии</li>
        <li>Альтернативная стоимость залога</li>
        <li>Экономия/переплата при выборе гарантии</li>
        <li>Рекомендация по выбору инструмента</li>
    </ul>
    
    <p><strong>Пример расчёта:</strong></p>
    <ul>
        <li>Сумма: 5 000 000 рублей</li>
        <li>Срок: 12 месяцев</li>
        <li>Премия: 2%</li>
        <li>Доходность: 10%</li>
        <li>Обслуживание: 5 000 руб./месяц</li>
    </ul>
    
    <p><strong>Результат:</strong></p>
    <ul>
        <li>Стоимость гарантии: 100 000 рублей</li>
        <li>Стоимость залога: 500 000 + 60 000 = 560 000 рублей</li>
        <li>Экономия при выборе гарантии: 460 000 рублей</li>
        <li>Рекомендация: Банковская гарантия</li>
    </ul>
</div>
