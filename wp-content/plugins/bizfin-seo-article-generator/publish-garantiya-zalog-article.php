    <h4>Кейс 3: Производственная компания</h4>
    <p><strong>Ситуация:</strong> ПАО "МеталлСервис" участвует в тендере на поставку оборудования стоимостью 50 млн рублей. Требуется обеспечение заявки на 2,5 млн рублей.</p>
    
    <p><strong>Варианты:</strong></p>
    <ul>
        <li>Залог производственного оборудования</li>
        <li>Банковская гарантия с премией 50 тыс. рублей (2%)</li>
    </ul>
    
    <p><strong>Анализ:</strong></p>
    <ul>
        <li>Оборудование не приносит дополнительного дохода</li>
        <li>Риски повреждения при использовании</li>
        <li>Сложность процедуры освобождения от залога</li>
    </ul>
    
    <p><strong>Решение:</strong> Выбрана банковская гарантия. Оборудование осталось в производственном процессе, компания избежала операционных рисков.</p>
</div>
