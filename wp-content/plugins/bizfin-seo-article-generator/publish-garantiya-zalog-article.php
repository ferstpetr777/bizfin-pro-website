<h2 id="faq">Часто задаваемые вопросы</h2>
<!-- /wp:heading -->

<!-- wp:html -->
<section class="faq-section">
    <div class="faq-item">
        <div class="faq-question">Когда лучше использовать депозит вместо гарантии?</div>
        <div class="faq-answer">Депозит как залог имеет смысл только в случаях, когда его доходность ниже премии по банковской гарантии. Обычно это происходит при очень низких процентных ставках по депозитам (менее 1-2% годовых) или при очень высоких премиях по гарантиям (более 5-7%). В большинстве случаев банковская гарантия экономически выгоднее.</div>
    </div>
    
    <div class="faq-item">
        <div class="faq-question">Как рассчитать альтернативную стоимость залогового имущества?</div>
        <div class="faq-answer">Альтернативная стоимость рассчитывается как доходность от наиболее выгодного альтернативного использования имущества. Для денежных средств это доходность депозита или инвестиций, для недвижимости — арендная доходность, для оборудования — доходность от производственного использования.</div>
    </div>
    
    <div class="faq-item">
        <div class="faq-question">Какие риски несет банковская гарантия?</div>
        <div class="faq-answer">Основные риски банковской гарантии: риск неплатежеспособности банка, риск отказа в выплате по формальным основаниям, риск изменения условий гарантии. Эти риски значительно ниже рисков потери залогового имущества, но их также необходимо учитывать при принятии решения.</div>
    </div>
    
    <div class="faq-item">
        <div class="faq-question">Можно ли комбинировать гарантию и залог?</div>
        <div class="faq-answer">Да, в некоторых случаях возможно комбинированное обеспечение. Например, частичное обеспечение банковской гарантией и частичное — залогом. Это может быть оправдано при ограниченных возможностях получения гарантии или при необходимости снижения премии за счет дополнительного обеспечения.</div>
    </div>
    
    <div class="faq-item">
        <div class="faq-question">Как влияет выбор инструмента на кредитоспособность?</div>
        <div class="faq-answer">Банковская гарантия не влияет на кредитоспособность, так как не блокирует активы. Залог снижает кредитоспособность, так как уменьшает объем свободных активов, которые могут быть использованы для получения дополнительного финансирования.</div>
    </div>
