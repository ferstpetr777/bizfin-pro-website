<p>Правильная подготовка документов — залог успешного получения банковской гарантии. Рассмотрим пошаговый алгоритм подготовки.</p>

<h3>Шаг 1: Анализ требований тендера</h3>

<p>Первым делом необходимо внимательно изучить документацию тендера и определить:</p>

<ul>
    <li>Размер обеспечения исполнения контракта</li>
    <li>Срок действия гарантии</li>
    <li>Особые требования к формулировкам</li>
    <li>Срок подачи заявки</li>
</ul>

<h3>Шаг 2: Сбор финансовых документов</h3>

<p>Банк потребует следующие финансовые документы:</p>

<div class="checklist">
    <h4>Финансовые документы для банковской гарантии:</h4>
    <ul>
