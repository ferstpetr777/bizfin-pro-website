        <li>Справка о состоянии расчетов с внебюджетными фондами</li>
    </ul>
</div>

<h3>Шаг 3: Подготовка документов по тендеру</h3>

<p>Обязательно предоставьте банку документы, связанные с конкретным тендером:</p>

<ul>
