    <li>Техническое задание</li>
    <li>Заявка на участие в тендере</li>
</ul>

<h3>Шаг 4: Проверка соответствия банку</h3>

<p>Убедитесь, что выбранный банк:</p>

<ul>
    <li>Включен в перечень банков для 44-ФЗ</li>
