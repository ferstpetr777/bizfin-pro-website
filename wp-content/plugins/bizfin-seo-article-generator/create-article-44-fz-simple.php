<h2 id="faq">Часто задаваемые вопросы</h2>

<div class="faq">
    <h4>Можно ли получить банковскую гарантию по 44-ФЗ в банке, не включенном в перечень?</h4>
    <p>Нет, это невозможно. Банк обязательно должен быть включен в перечень банков, соответствующих требованиям для принятия банковских гарантий в целях налогообложения. Этот перечень публикуется на сайте Минфина России и регулярно обновляется.</p>
</div>

<div class="faq">
    <h4>Какой минимальный срок действия должен быть у банковской гарантии?</h4>
    <p>Согласно 44-ФЗ, срок действия банковской гарантии должен превышать срок действия контракта не менее чем на один месяц. Это требование обязательно для всех видов банковских гарантий.</p>
</div>

<div class="faq">
    <h4>Можно ли изменить условия банковской гарантии после ее выдачи?</h4>
    <p>Банковская гарантия является безотзывным обязательством. Изменение условий после выдачи невозможно. Если требуется изменение условий, необходимо получить новую гарантию.</p>
</div>

<div class="faq">
    <h4>Что делать, если банк отказывает в выдаче гарантии?</h4>
    <p>При отказе банка необходимо выяснить причину отказа и устранить выявленные недостатки. Можно обратиться в другой банк из перечня или улучшить финансовые показатели компании. Рекомендуется обращаться в несколько банков одновременно.</p>
</div>

<div class="faq">
    <h4>Обязательно ли регистрировать банковскую гарантию в реестре?</h4>
    <p>Да, регистрация в реестре банковских гарантий обязательна. Без регистрации гарантия не будет принята заказчиком. Регистрацию обычно осуществляет банк-гарант в течение 1 рабочего дня после выдачи гарантии.</p>
</div>

<div class="faq">
    <h4>Можно ли использовать одну банковскую гарантию для нескольких контрактов?</h4>
    <p>Нет, каждая банковская гарантия привязана к конкретному контракту. Для каждого контракта необходимо получать отдельную гарантию с указанием всех реквизитов конкретного контракта.</p>
</div>

<div class="faq">
    <h4>Как проверить подлинность банковской гарантии?</h4>
    <p>Подлинность банковской гарантии можно проверить в реестре банковских гарантий на сайте Единой информационной системы в сфере закупок (zakupki.gov.ru). Также можно обратиться напрямую в банк-гарант для подтверждения.</p>
</div>

<div class="faq">
    <h4>Что происходит с банковской гарантией при досрочном расторжении контракта?</h4>
    <p>При досрочном расторжении контракта банковская гарантия прекращает свое действие. Банк возвращает залогодателю часть комиссии пропорционально неиспользованному сроку действия гарантии, если это предусмотрено договором.</p>
</div>

