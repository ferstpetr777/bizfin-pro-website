    <li><strong>Качество активов</strong> — доля проблемных кредитов и вложений</li>
    <li><strong>Управление рисками</strong> — система риск-менеджмента и внутреннего контроля</li>
    <li><strong>Репутация и история</strong> — опыт работы и репутация на рынке</li>
    <li><strong>Поддержка акционеров</strong> — возможность получения дополнительного капитала</li>
</ul>

