    <li><strong>Обратиться к постоянному банку</strong> — использовать существующие отношения</li>
    <li><strong>Использовать онлайн-сервисы</strong> — ускорить подачу и рассмотрение</li>
    <li><strong>Предоставить дополнительное обеспечение</strong> — снизить риски банка</li>
