<!-- wp:heading {"level":3} -->
<h3>Наиболее частые причины отказов</h3>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p><strong>1. Нарушение сроков</strong></p>
<p>Самая распространенная причина — предъявление требования после истечения срока действия гарантии или срока для предъявления требований.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p><strong>2. Неполный комплект документов</strong></p>
<p>Отсутствие обязательных документов или их неправильное оформление. Банк вправе отказать, если не может проверить обоснованность требования.</p>
