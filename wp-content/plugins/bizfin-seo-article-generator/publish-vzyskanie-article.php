<p><strong>3. Несоответствие требования условиям гарантии</strong></p>
<p>Требование не соответствует основаниям, указанным в гарантии, или сумма превышает лимит гарантии.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p><strong>4. Нарушение процедуры предъявления</strong></p>
<p>Неправильный способ подачи документов, отсутствие уведомления принципала или нарушение других процедурных требований.</p>
