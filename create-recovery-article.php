<h2 id="grounds">Основания для взыскания</h2>

<p>Взыскание по банковской гарантии на возврат аванса возможно только при наличии законных оснований, предусмотренных условиями гарантии и договором между заказчиком и исполнителем. Понимание этих оснований поможет правильно оформить требование и избежать отказов банка.</p>

<div class="structure-item">
    <h4>Основные основания для взыскания</h4>
    <p>Банк обязан выплатить сумму гарантии при: неисполнении исполнителем обязательств по договору, нарушении сроков выполнения работ, некачественном выполнении работ, отказе от выполнения договора, банкротстве исполнителя, нарушении условий договора, нецелевом использовании авансовых средств.</p>
</div>

<div class="structure-item">
    <h4>Условия взыскания</h4>
    <p>Для взыскания необходимо: наличие действующей банковской гарантии, соблюдение сроков предъявления требования, предоставление полного пакета документов, соответствие требования условиям гарантии, отсутствие нарушений со стороны бенефициара, соблюдение процедуры уведомления исполнителя.</p>
</div>

<div class="structure-item">
    <h4>Документальные основания</h4>
    <p>Основания должны быть подтверждены документами: актом о неисполнении обязательств, претензией к исполнителю, уведомлением о нарушении договора, экспертным заключением о качестве работ, справкой о банкротстве исполнителя, документами о нецелевом использовании средств.</p>
</div>

<div class="structure-item">
