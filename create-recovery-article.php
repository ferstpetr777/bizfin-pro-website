<h2 id="timing">Сроки взыскания</h2>

<p>Сроки взыскания по банковской гарантии на возврат аванса строго регламентированы законодательством и условиями гарантии. Соблюдение сроков является критически важным для успешного получения средств от банка.</p>

<div class="structure-item">
    <h4>Сроки предъявления требования</h4>
    <p>Требование о выплате должно быть предъявлено банку в течение срока действия гарантии. Обычно срок составляет 30-90 дней с момента наступления события, дающего право на взыскание. Пропуск срока может привести к отказу в выплате.</p>
</div>

<div class="structure-item">
    <h4>Сроки рассмотрения требования банком</h4>
    <p>Банк обязан рассмотреть требование и принять решение в течение 5-30 дней в зависимости от условий гарантии. При положительном решении банк должен перечислить средства в течение 3-5 рабочих дней. При отказе банк обязан мотивировать свое решение.</p>
</div>

<div class="structure-item">
    <h4>Сроки судебного обжалования</h4>
    <p>При отказе банка в выплате бенефициар может обратиться в суд в течение 3 лет с момента отказа. Срок исковой давности может быть приостановлен при проведении переговоров или досудебного урегулирования спора.</p>
</div>

<div class="structure-item">
    <h4>Факторы, влияющие на сроки</h4>
    <p>На сроки влияют: сложность дела, полнота документов, загруженность банка, необходимость дополнительных проверок, судебные разбирательства, форс-мажорные обстоятельства, изменения в законодательстве.</p>
</div>

<div class="warning">
