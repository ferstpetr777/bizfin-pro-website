<h2 id="faq">Часто задаваемые вопросы</h2>

<div class="faq-section">
    <div class="faq-item">
        <div class="faq-question">Как учитывать аванс при составлении актов КС-2 и КС-3?</div>
        <div class="faq-answer">При составлении актов КС-2 и КС-3 аванс учитывается следующим образом: в акте КС-2 отражается объем выполненных работ, в справке КС-3 указывается стоимость выполненных работ, аванс засчитывается в счет выполненных работ, неосвоенная часть аванса остается на балансе подрядчика, банковская гарантия покрывает неосвоенную часть аванса, при завершении работ аванс полностью засчитывается в счет выполненных работ.</div>
    </div>
    
    <div class="faq-item">
        <div class="faq-question">Что делать, если задержались поставки материалов?</div>
        <div class="faq-answer">При задержке поставок материалов необходимо: уведомить заказчика о задержке, обосновать причины задержки, предложить альтернативные варианты поставок, скорректировать график выполнения работ, согласовать новые сроки с заказчиком, принять меры по ускорению поставок, минимизировать влияние задержки на общие сроки.</div>
    </div>
    
    <div class="faq-item">
        <div class="faq-question">Можно ли изменить график освоения аванса?</div>
