<h2 id="control-mechanisms">Контроль освоения средств</h2>

<p>Контроль освоения авансовых средств в строительстве осуществляется через систему документооборота, регулярных проверок и отчетности. Эффективный контроль помогает предотвратить нецелевое использование средств и обеспечивает соблюдение условий банковской гарантии.</p>

<div class="structure-item">
    <h4>Механизмы контроля</h4>
    <p>Основные механизмы включают: ежемесячную отчетность о выполненных работах, акты выполненных работ (КС-2, КС-3), справки о стоимости выполненных работ, фотографии выполненных работ, отчеты о расходовании средств, проверки представителей заказчика, аудит финансовой отчетности.</p>
</div>

<div class="structure-item">
    <h4>Роли участников контроля</h4>
    <p>В контроле участвуют: заказчик (контроль качества и сроков), подрядчик (отчетность и документооборот), банк-гарант (контроль финансового состояния), технический надзор (контроль качества работ), независимые эксперты (оценка выполненных работ), государственные органы (контроль соблюдения норм).</p>
</div>

<div class="structure-item">
    <h4>Документы контроля</h4>
    <p>Основные документы: график выполнения работ, смета расходов, акты выполненных работ, справки о стоимости, отчеты о расходовании средств, фотографии работ, акты приемки этапов, справки о качестве, документы о поставках, сертификаты материалов.</p>
</div>

<div class="structure-item">
    <h4>Нарушения и санкции</h4>
    <p>При нарушениях применяются: штрафы за нарушение сроков, штрафы за некачественное выполнение, штрафы за нецелевое использование средств, приостановка финансирования, требование возврата неосвоенных средств, расторжение контракта, взыскание по банковской гарантии.</p>
</div>

<div class="warning">
