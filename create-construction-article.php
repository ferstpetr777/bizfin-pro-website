    <h4>Этапы освоения аванса</h4>
    <ol>
        <li>Подготовительный этап (10-15% аванса)</li>
        <li>Нулевой цикл (15-25% аванса)</li>
        <li>Возведение коробки (30-40% аванса)</li>
        <li>Инженерные системы (15-20% аванса)</li>
        <li>Отделочные работы (10-15% аванса)</li>
        <li>Благоустройство (5-10% аванса)</li>
        <li>Сдача объекта (остаток аванса)</li>
    </ol>
</div>

<div class="structure-item">
    <h4>Принципы планирования графика</h4>
    <p>При планировании графика учитываются: технологическая последовательность работ, сезонность строительства, поставки материалов и оборудования, получение разрешений, погодные условия, возможности подрядчика, требования заказчика, нормативные сроки.</p>
</div>

<div class="structure-item">
    <h4>Контрольные точки</h4>
    <p>Контрольные точки включают: завершение подготовительных работ, завершение нулевого цикла, возведение несущих конструкций, монтаж инженерных систем, завершение отделочных работ, благоустройство территории, получение разрешения на ввод в эксплуатацию.</p>
</div>

<div class="structure-item">
    <h4>Документирование освоения</h4>
